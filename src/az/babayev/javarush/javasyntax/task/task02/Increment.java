/*
Используя только оператор инкремента, измени значение в переменной six,
 чтобы на экран вывелась цифра 9.
 */
public class Increment {
    public static void main(String[] args) {
        int six = 6;
        six ++;
        six ++;
        six ++;
        System.out.println(six);
    }
}
