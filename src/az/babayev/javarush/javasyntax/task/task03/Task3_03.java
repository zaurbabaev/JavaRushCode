package az.babayev.javarush.javasyntax.task.task03;

/*
Добавь метод public static void printString(String s),
в нем напиши код, который будет выводить переданную строку на экран.
 */
public class Task3_03 {
    public static void printString(String s) {
        System.out.println(s);
    }

    public static void main(String[] args) {
        printString("Hello, Amigo!");
    }
}
