package az.babayev.javarush.javasyntax.repetition.wraper_type;

public class CharacterExample {
    public static void main(String[] args) {
        System.out.println(Character.isAlphabetic(65));
        System.out.println(Character.isDigit('1'));
        System.out.println(Character.isUpperCase('I'));
        System.out.println(Character.isLowerCase('I'));
        System.out.println(Character.isWhitespace('\n'));
        System.out.println(Character.isSpaceChar('\n'));
        System.out.println(Character.toLowerCase('I'));

       
    }
}
